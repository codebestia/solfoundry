      expect(record.contributor).to.be.null;
      expect(record.githubIssue).to.equal(githubIssue);
      expect(record.githubPr).to.equal("");
      expect(record.reviewScores).to.deep.equal([]);
      expect(record.finalScore).to.equal(0);
      expect(record.prHash).to.be.null;
      expect(record.createdAt.toNumber()).to.be.greaterThan(0);
      expect(record.updatedAt.toNumber()).to.be.greaterThan(0);
      expect(record.completedAt).to.be.null;
      expect(record.bump).to.be.a("number");
