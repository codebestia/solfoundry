      const bountyId = getNextBountyId();
      await registerDefaultBounty(bountyId);
      await advanceTo(bountyId, 2);

      const [bountyPda] = derivePda(bountyId, program.programId);

      try {
        await program.methods
          .updateStatus(4, null)
          .accounts({
            admin: admin.publicKey,
            bountyRecord: bountyPda,
          })
          .rpc();
        expect.fail("Expected error for invalid transition from InReview");
      } catch (error: any) {
        expect(error).to.be.instanceOf(AnchorError);
        expect(error.error.errorCode.code).to.equal(
          "InvalidStatusTransition"
        );
      }
