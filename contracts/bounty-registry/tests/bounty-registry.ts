      const [bountyPda] = derivePda(bountyId, program.programId);

      const title = "Fix authentication bypass vulnerability";
      const tier = 3;
      const rewardAmount = 1_000_000;
      const githubIssue =
        "https://github.com/SolFoundry/solfoundry/issues/600";

      await program.methods
        .registerBounty(
          new BN(bountyId),
          title,
          tier,
          new BN(rewardAmount),
          githubIssue
        )
        .accounts({
          admin: admin.publicKey,
          bountyRecord: bountyPda,
          systemProgram: SystemProgram.programId,
        })
        .rpc();

      // Fetch and verify the on-chain record.
