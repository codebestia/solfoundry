      expect(record.status).to.deep.include({ completed: {} });
      expect(record.githubPr).to.equal(githubPr);
      expect(record.reviewScores).to.deep.equal(reviewScores);
      expect(record.finalScore).to.equal(finalScore);
      expect(record.prHash).to.deep.equal(prHash);
      expect(record.completedAt).to.not.be.null;
